def get_rag_response(question: str, chunk_metadata: dict, k: int) -> dict:
    try:
        payload = {
            "message": question,
            "selected_context": [{
                "course": chunk_metadata.get("course", "Unknown"),
                "degree": chunk_metadata.get("degree", "Unknown"),
                "source": chunk_metadata.get("source", "Unknown"),
            }],
            "chat_history": []
        }

        response = requests.post(
            f"{BACKEND_URL}/chat?context=True&k={k}&use_multiquery=True",
            json=payload,
            timeout=300,
        )
        response.raise_for_status()
        data = response.json()

        return {
            "response": data.get("response", "No response from RAG"),
            "context": data.get("context", []),
            "latency": data.get("generation_latency", -1),  # ← del backend
        }

    except Exception as e:
        return {"response": f"RAG Error: {str(e)}", "context": [], "latency": -1}
 
 
# ==========================================
# EXPERIMENT LOOP
# ==========================================
 
def run_experiment():
    print("=" * 60)
    print("  H6 Experiment: Optimal Context Size (k)")
    print("=" * 60)
 
    # Load base dataset
    with open(BASE_DATASET_PATH, "r", encoding="utf-8") as f:
        base_data = json.load(f)
 
    # Filter out out_of_scope (no valid ground truth for evaluation)
    #base_data = [item for item in base_data if item.get("ground_truth") != "out_of_scope"]
    print(f"Base dataset loaded: {len(base_data)} items (out_of_scope filtered)")
 
    for k in K_VALUES:
        print(f"\n{'='*60}")
        print(f"  Running experiment for k={k}")
        print(f"{'='*60}")
 
        dataset_k = []
 
        for i, item in enumerate(base_data):
            print(f"  [{i+1}/{len(base_data)}] k={k} | {item['question'][:60]}...")
 
            res = get_rag_response(
                question=item["question"],
                chunk_metadata=item.get("chunk_metadata", {}),
                k=k,
            )
 
            new_item = item.copy()
            new_item["answer"]   = res["response"]
            new_item["contexts"] = res["context"]
            new_item["latency"]  = res["latency"]
 
            dataset_k.append(new_item)
 
        # Save dataset for this k
        dataset_path = f"datasets/dataset_k{k}_multi.json"
        with open(dataset_path, "w", encoding="utf-8") as f:
            json.dump(dataset_k, f, ensure_ascii=False, indent=4)
        print(f"\nDataset k={k} saved to {dataset_path}")
 
        # Evaluate
        run_evaluation(
            dataset_path=dataset_path,
            output_csv=f"results/evaluation_k{k}_multi_summ.csv",
            sample_percentage=1,  # evaluate on 100% of the dataset for speed; adjust as needed
        )
 
    print("\n" + "=" * 60)
    print("  H6 Experiment completed.")
    print(f"  Results saved in: results/")
    print("=" * 60)
 
 
if __name__ == "__main__":
    run_experiment()